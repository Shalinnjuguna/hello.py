def add(a,b):
	answer=a+b
	return answer

def subtract(a,b):
	answer=a-b
	return answer

def wholenumber(b,c):
	answer=b//c
	return answer

def exponent(b,c):
	answer=b**c 
	return answer	

def percentage(c,d):
	answer=c%d
	return answer

def divide(c,d):
	answer=c/d
	return answer

def modulus(c,d):
	answer=c%d		
	return answer

def multiply(c,d):
	answer=c*d
	return answer
